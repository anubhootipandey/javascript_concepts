let isNotEqual = (5 != 3);
let isGreater = (5 > 3);
let isSmaller = (5 < 3);
let isGreaterOrEqual = (5 >= 5);
let isSmallerOrEqual = (5 <= 3);

// 3--> Logical Operators
let isBothTrue = (5 > 3 && 2 < 4); //AND
let isOneTrue = (5 > 3 || 2 > 4); //OR
let isNotTrue = !(5 > 3); //Not

