const myMap = new Map();
myMap.set('key1', 'value1');
myMap.set('key2', 'value2');
console.log(myMap);

const mySet = new Set();
mySet.add('value1');
mySet.add('value2');
console.log(mySet);

//Map:- used to store key-value pairs where each key is unique.
//Set:- used to store unique values of any type, where each value is unique.